public class EscapeSequence {
    public static void main(String[] args) {
        System.out.println("\"Hello World\"");
        //output: "Hello World"

        System.out.println("Use of backslash :\\");
        //output: Use of backslash: :\

        System.out.println("Use of newlline char: \nIn new line :D");

    }
}
